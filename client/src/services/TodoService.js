export default {
    getTodos : ()=>{
        return fetch('/user/todos')
                .then(response=>{
                    if(response.status !== 401){
                        return response.json().then(data => data);
                    }
                    else
                        return {message : {msgBody : "UnAuthorized",msgError : true}};
                });
    },
    postTodo : todo=>{
        return fetch('/user/todo',{
            method : "post",
            body : JSON.stringify(todo),
            headers:{
                'Content-Type' : 'application/json'
            }
        }).then(response=>{
            if(response.status !== 401){
                return response.json().then(data => data);
            }
            else
                return {message : {msgBody : "UnAuthorized"},msgError : true};
        });
    },
    deleteTodo : id =>{
        return fetch('/user/todos/' + id ,{
            method : "delete",
        }).then(response=>{
            if(response.status !== 401){
                return response.json().then(data => data);
            }
            else
                return {message : {msgBody : "UnAuthorized"},msgError : true};
        });
    },
    getTodo : id =>{
        return fetch('/user/todos/' + id ,{
            method : "get",
        }).then(response=>{
            if(response.status !== 401){
                return response.json().then(data => data);
            }
            else
                return {message : {msgBody : "UnAuthorized"},msgError : true};
        });
    },
    updateTodo : (todo,id) =>{
        return fetch('/user/todos/' + id,{
            method : "put",
            body : JSON.stringify(todo),
            headers:{
                'Content-Type' : 'application/json'
            }
        }).then(response=>{
            if(response.status !== 401){
                return response.json().then(data => data);
            }
            else
                return {message : {msgBody : "UnAuthorized"},msgError : true};
        });
    }
}